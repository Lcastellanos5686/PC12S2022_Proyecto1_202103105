    private void BotonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAceptarActionPerformed
        // TODO add your handling code here:
        if (Integer.valueOf(TextoMonto.getText()) < 0 || Integer.valueOf(TextoMonto.getText()) == 0) {
            JOptionPane.showMessageDialog(null, "La cantidad que ha introducido no es valida", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            MenuPrincipal.ListaDeCuentas[CuentaCombo.getSelectedIndex() + 1].Saldo = MenuPrincipal.ListaDeCuentas[CuentaCombo.getSelectedIndex() + 1].Saldo + Integer.valueOf(TextoMonto.getText());
            MenuPrincipal.ListaDeCuentas[CuentaCombo.getSelectedIndex() + 1].SaldoCuenta();
            JOptionPane.showMessageDialog(null, "Deposito realizado con éxito", "Advertencia", JOptionPane.INFORMATION_MESSAGE);
            for (int i = 0; i < 25; i++) {
            if (MenuPrincipal.Historial[CuentaCombo.getSelectedIndex() + 1][0][i] == null) {
                
                MenuPrincipal.Historial[CuentaCombo.getSelectedIndex() + 1][0][i] = String.valueOf(MenuPrincipal.IDHistorial);
                MenuPrincipal.IDHistorial++;
                MenuPrincipal.Historial[CuentaCombo.getSelectedIndex() + 1][1][i] = fecha2.format(LocalDateTime.now());;
                MenuPrincipal.Historial[CuentaCombo.getSelectedIndex() + 1][2][i] = "Deposito";
                MenuPrincipal.Historial[CuentaCombo.getSelectedIndex() + 1][3][i] = "0";
                MenuPrincipal.Historial[CuentaCombo.getSelectedIndex() + 1][4][i] = TextoMonto.getText();
                MenuPrincipal.Historial[CuentaCombo.getSelectedIndex() + 1][5][i] = String.valueOf(MenuPrincipal.ListaDeCuentas[CuentaCombo.getSelectedIndex() + 1].Saldo);
                
                break;
            }
        }
            dispose();
        }

        


    }//GEN-LAST:event_BotonAceptarActionPerformed

