    public static String[][] ParaComboBox2() {
        int contador = 0;
        for (int i = 1; i < ListaDeCuentas.length; i++) {
            if (ListaDeCuentas[i] == null) {
                break;
            }
            contador++;
        }
        String[][] TextoEnCombo = new String[2][contador];
        for (int i = 1; i < contador + 1; i++) {
            TextoEnCombo[0][i - 1] = "ID: " + ListaDeCuentas[i].IDcuenta + " | Dueño: " + ListaDeCuentas[i].Dueno.Nombre + " " + ListaDeCuentas[i].Dueno.Apellido;
            TextoEnCombo[1][i - 1] = String.valueOf(ListaDeCuentas[i].IDcuenta);

        }
        return TextoEnCombo;
    }

