    public static String[][] ParaTabla() {
        int DimTabla = 0;
        for (int i = 0; i < 5; i++) {
            if (MenuPrincipal.ListaDeClientes[i] == null) {
                break;
            }
            DimTabla++;
        }

        String TablaCliente[][] = new String[DimTabla][3];

        for (int i = 0; i < DimTabla; i++) {
            for (int j = 0; j < 3; j++) {
                switch (j) {
                    case 0:
                        TablaCliente[i][j] = Long.toString(MenuPrincipal.ListaDeClientes[i].DPI);

                        break;
                    case 1:
                        TablaCliente[i][j] = MenuPrincipal.ListaDeClientes[i].Nombre;

                        break;
                    case 2:
                        TablaCliente[i][j] = MenuPrincipal.ListaDeClientes[i].Apellido;

                        break;
                }

            }

        }
        return TablaCliente;
    }

    public static String[][] ParaTabla2(Long DPI) {
        int DimTabla = 0;
        int num = 0;

        for (int i = 1; i < 26; i++) {//Identifica cuantas cuentas tiene
            if (MenuPrincipal.ListaDeCuentas[i] == null) {
                break;
            } else if (MenuPrincipal.ListaDeCuentas[i].Dueno.DPI == DPI) {
                DimTabla++;
            }
        }

        System.out.println(DimTabla);
        String TablaCuenta[][] = new String[DimTabla][1];

        for (int j = 1; j < 26; j++) {
            if (MenuPrincipal.ListaDeCuentas[j] == null) {
                System.out.println("ITJ busca null" + j);
                break;
            } else if (MenuPrincipal.ListaDeCuentas[j].Dueno.DPI == DPI) {
                TablaCuenta[num][0] = "ID " + Integer.toString(j);
                System.out.println("ITJ " + j);
                num++;
            }

        }
        return TablaCuenta;
    }

