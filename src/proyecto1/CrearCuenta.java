    private void BotonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAceptarActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i < 5; i++) {
            MenuPrincipal.CuentasPorCliente[i][0] = i;
        }

        if (MenuPrincipal.CuentasPorCliente[ClientesCombo.getSelectedIndex()][1] > 4) {
            JOptionPane.showMessageDialog(null, "El cliente ha superado el número de cuentas permitido", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            Cuenta nueva = new Cuenta(ListaDeClientes[ClientesCombo.getSelectedIndex()], MenuPrincipal.IDcuenta);
            MenuPrincipal.ListaDeCuentas[MenuPrincipal.IDcuenta] = nueva;
            MenuPrincipal.IDcuenta++;
            MenuPrincipal.CuentasPorCliente[ClientesCombo.getSelectedIndex()][1]++;
            JOptionPane.showMessageDialog(null, "Cuenta creada con éxito", "Advertencia", JOptionPane.INFORMATION_MESSAGE);
            MenuPrincipal.ListaDeCuentas[MenuPrincipal.IDcuenta-1].DatosCuentaCliente(ClientesCombo.getSelectedIndex());
            dispose();
        }


    }//GEN-LAST:event_BotonAceptarActionPerformed

