    public static String[] ParaComboBox() {
        int contador = 0;
        for (int i = 0; i < ListaDeClientes.length; i++) {
            if (ListaDeClientes[i] == null) {
                break;
            }
            contador++;
        }
        String[] TextoEnCombo = new String[contador];
        for (int i = 0; i < contador; i++) {
            TextoEnCombo[i] = (i + 1) + ". " + ListaDeClientes[i].Nombre + " " + ListaDeClientes[i].Apellido + " | " + ListaDeClientes[i].DPI;
        }
        return TextoEnCombo;
    }

