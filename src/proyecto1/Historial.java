    public static String[][] ParaTabla2(int cuenta) {
        String[][] Tabla = new String[25][6];

        for (int i = 0; i < 25; i++) {
            for (int j = 0; j < 6; j++) {
                if (MenuPrincipal.Historial[1][0][i] == null) {
                    break;
                }
                Tabla[i][j] = MenuPrincipal.Historial[cuenta][j][i];
            }
        }

        return Tabla;
    }
