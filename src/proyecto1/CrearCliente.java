    private void BotonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAceptarActionPerformed
        // TODO add your handling code here:
        boolean lleno = true;
        boolean Duplicado = false;

        for (int i = 0; i < 5; i++) {
            if (MenuPrincipal.ListaDeClientes[i] == null) {
                break;
            } else if (MenuPrincipal.ListaDeClientes[i].DPI == Long.parseLong(CampoDPI.getText())) {
                Duplicado = true;
            }
        }

        if (CampoDPI.getText().length() != 13) {
            JOptionPane.showMessageDialog(null, "La longitud del CUI es incorrecta", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else if (Duplicado) {
            JOptionPane.showMessageDialog(null, "El nÃºmero del CUI ya existe en la base de datos", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            Cliente nuevo = new Cliente(CampoNombre.getText(), CampoApellido.getText(), Long.parseLong(CampoDPI.getText()));

            for (int i = 0; i < 5; i++) {
                if (MenuPrincipal.ListaDeClientes[i] == null) {
                    MenuPrincipal.ListaDeClientes[i] = nuevo;
                    lleno = false;
                    break;
                }
            }
            if (lleno) {
                JOptionPane.showMessageDialog(null, "La lista de clientes se ha llenado", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
            System.out.println("");
            for (int i = 0; i < MenuPrincipal.ListaDeClientes.length; i++) {
                if (MenuPrincipal.ListaDeClientes[i] == null) {
                    break;
                } else {
                    MenuPrincipal.ListaDeClientes[i].Imprime();
                }
            }
            System.out.println("");
            JOptionPane.showMessageDialog(null, "Cliente creado con exito", "Advertencia", JOptionPane.INFORMATION_MESSAGE);
            CampoDPI.setText("");
            CampoNombre.setText("");
            CampoApellido.setText("");
            dispose();
        }


    }//GEN-LAST:event_BotonAceptarActionPerformed

